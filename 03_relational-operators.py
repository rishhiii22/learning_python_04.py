# Relation operators in Python
# In Python, relational operators are used to compare two values. The result of a comparison is either True or False.

a = 10
b = 20

print(a == b)  # Output: False (a is not equal to b)
print(a != b)  # Output: True (a is not equal to b)
print(a < b)   # Output: True (a is less than b)
print(a > b)   # Output: False (a is not greater than b)
print(a <= b)  # Output: True (a is less than or equal to b)
print(a >= b)  # Output: False (a is not greater than or equal to b)